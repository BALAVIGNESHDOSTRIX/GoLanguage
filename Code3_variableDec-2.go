package main

import "fmt"

func main(){

		//string declaration
		var name string
		name = "Balavignesh"

		//integer declaration
		var a int64 = 100

		//float declaration
		var foo1 float32 = 12.4

		//unsigned declaration
		var foo2 uint64 = 200

		//multiple value declaration

		var foo3,foo4 int64 = 320,600





		fmt.Printf("My name is: %s \n",name )
		fmt.Printf("a value is: %d \n",a)
		fmt.Printf("foo1 value is: %f \n",foo1)
		fmt.Printf("foo2 value is: %d \n",foo2)
		fmt.Printf("foo3 value is: %d \n",foo3)
		fmt.Printf("foo4 value is: %d \n",foo4)
}